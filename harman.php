			<p>To understand the user population's attitudes, motivations, and behaviors, we conducted four interviews with members of the target user population: drivers and passengers that interact with car infotainment systems. </p>
			<p>My team held interpretation sessions where we extracted key findings and patterns from our interviews onto sticky notes. We created an affinity wall, putting notes with common themes together so we could see the broader picture and relationships and ultimately generate recommendations.</p>
		</section>

		<section class="portfolio-section">
			<h3>Surveys</h3>
			<img src="img/surveyboard622.jpg" alt="" width="50%">
			<p>With the key findings from our interviews in mind, we designed survey questions to assess how prevalent driver behaviors and attitudes were and to learn about the user demographics. From our 154 survey responses, we learned of four key findings related to preferred infotainment interaction interfaces, most commonly used infotainment features, and attitudes about phone-car connections.</p>
		</section>

		<section class="portfolio-section">
			<h3>Personas and Scenarios</h3>
			<a href="img/personas622.jpg" data-lightbox="harman" data-title="Images of our personas">
				<img src="img/personas622.jpg" alt="Images of our Personas" width="50%">
			</a>
			<aside>Graphics by Raden</aside>
			<p>Our surveys and interviews helped us understand the user population and the contexts in which users interact with infotainment systems. We summarized this information into personas and scenarios, which are design artifacts we constructed to guide our thinking during the evaluation process. Next, we turned to heuristic evaluations and usability testing to assess the usability of Harman's product. </p>

		</section>
		<section class="portfolio-section">
			<h3>Heuristic Evaluation</h3>
			<img src="https://public-media.interaction-design.org/images/uploads/bc407d74dd91972a7370c0e22cfacc36.jpg" alt="" width="50%">
			<aside>Image from the Interaction Design Foundation</aside>
			<p>Using Nielsen's (1994) ten Heuristics, each member of my team evaluated Harman's system and rated the level of severity of the usability issue. We then aggregated our findings and prioritized them before describing them in our report.</p>
		</section>

		<section class="portfolio-section">
			<h3>Usability Testing</h3>
			<img src="img/usabilitytest622.jpg" alt="usability test methods" width="35%">
			<p>The last research method we conducted was usability testing with five users. We created five tasks for users to complete using Harman's system, and we asked the participants to think aloud about their thought process and expectations while they did it. We observed and recorded comments, errors, and notable incidents. With our notes and responses from questionnaires we gave them, we concluded four key findings related to the phone-car connection process, home screen interface, and radio interface.</p>
